struct Node{
    ll max = 0;
    ll pos = -1;
    ll add = 0;
};
const int OPEN = -1, CLOSE = 1;
struct Event{
    int x, type, l, r;
    bool operator<(const Event& oth) const{
        return tie(x, type, l, r) < tie(oth.x, oth.type, oth.l, oth.r);
    }
};
vector<Node> st;

pll NEUTRAL = {INT_MIN, 0};
void push(int x, int l, int r){
    st[x].max += st[x].add;
    // cout << l << ' ' << r << ' ' << st[x].add << ' ' << st[x].sum << '\n';
    if (r - l != 1){
        st[2 * x].add += st[x].add;
        st[2 * x + 1].add += st[x].add;
    }   
    st[x].add = 0;
}

void pull(int x, int l, int r){
    if (r - l != 1){
        st[x].max = st[2 * x].max;
        st[x].pos = st[2 * x].pos;
        if (st[x].max < st[2 * x + 1].max){
            st[x].max = st[2 * x + 1].max;
            st[x].pos = st[2 * x + 1].pos;
        }
    }
}

pll get(int x, int l, int r, int L, int R){ // x - index, l, r - now, L, R - need
    push(x, l, r);
    if (l >= R || r <= L) return NEUTRAL;
    // print(cout, l, r, L, R);
    if (L <= l && r <= R) {
        return {st[x].max, st[x].pos};
    }
    int m = (l + r) / 2;
    auto lft = get(2 * x, l, m, L, R);
    auto rgt = get(2 * x + 1, m, r, L, R);
    // cerr << l << ' ' << r << ' ' << L << ' ' << R << " "<< lft << ' ' << rgt << '\n';
    pull(x, l, r);
    if (lft.first > rgt.first) return lft;
    return rgt;
}

void insert(int x, int l, int r, int where, ll what){
    push(x, l, r);
    if (where < l || where >= r) return;
    if (where == l && where == r - 1){
        st[x].max = what;
        return;
    }
    int m = (l + r) / 2;
    insert(2 * x, l, m, where, what);
    insert(2 * x + 1, m, r, where, what);
    pull(x, l, r);
}

void insert_add(int x, int l, int r, int L, int R, int add){
    push(x, l, r);
    if (l >= R || r <= L) return;
    if (L <= l && r <= R){
        st[x].add += add;
        push(x, l, r);
        return;
    }
    int m = (l + r) / 2;
    insert_add(2 * x, l, m, L, R, add);
    insert_add(2 * x + 1, m, r, L, R, add);
    pull(x, l, r);
}
struct Solver{
    int n, k;
    Solver(){
        n = 2e6 + 5;    
        int power = 1;
        while ((1 << power) < n) power++;
        int N = 1 << power;
        st.resize(2 * N);
        int uns = 1e6;
        for (int i = 0;i <= n; i++){
            st[i + N].pos = i - uns;
        }
        cin >> k;
        vector<Event> events(2 * k);
        for (int i = 0;i < k;i++){
            int x1, y1, x2, y2;
            cin >> x1 >> y1 >> x2 >> y2;
            events[2 * i] = {x1, OPEN, y1, y2 + 1};
            events[2 * i + 1] = {x2, CLOSE, y1, y2 + 1};
        }
        for (int i = N - 1; i > 0;i--){
            pull(i, 0, N);
        }
        sort(all(events));
        int ans = 0, ans_x = -1e6, ans_y = -1e6;
        for (const auto& e : events){
            if (e.type == OPEN){
                insert_add(1, 0, N, e.l + uns, e.r + uns, 1);
            }
            auto tmp = get(1, 0, N, e.l + uns, e.r + uns);
            // print(cout, "at x", e.x, "max is", tmp.first, "at", tmp.second);
            // print(cout, "now rectangle", e.x, e.l, e.r, "and it is", (e.type == OPEN ? "OPEN" : "CLOSE"));
            if (tmp.first > ans){
                ans_x = e.x;
                ans_y = tmp.second;
                ans = tmp.first;
            }
            if (e.type == CLOSE){
                insert_add(1, 0, N, e.l + uns, e.r + uns, -1);
            }
        }
        cout << ans << '\n' << ans_x << ' ' << ans_y << '\n'; 
    }   
};

