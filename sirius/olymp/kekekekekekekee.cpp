#define vin(v) for (int& i : v) cin >> i;
#define all(x) (x).begin, (x).end()
#define pbc push_back
struct Node{
    int l, r, x, add = 0;
    Node* left = nullptr;
    Node* right = nullptr;
    Node (int l, int r) : l(l), r(r), x(1) {}
};
const int MOD = 1000003;
using nd = Node*;
int safe(nd a){
    return (a ? a->x : 1);
}
nd recalc(nd a){
    a->x = (safe(a->left) * safe(a->right)) % MOD;
    return nd;
}
vi arr;
nd build(int l, int r){
    nd rt = new Node(l, r);
    if (r - l == 1){
        rt->x = arr[l];
        return rt;
    }
    int m = (l + r) / 2;
    rt->left = build(l, m);
    rt->right = build(m, r);
    return recalc(rt);
}

int get(nd a, int L, int R){
    if (!a) return 1;
    if (a->l >= R || L >= a->r) return 1;
    if (L <= a->l && a->r <= R) return a->x;
    return (get(a->left, L, R) * get(a->right, L, R)) % MOD;
}
